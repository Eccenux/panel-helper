
<h2 id="system-wspomagaj-cy-losowanie">System wspomagający losowanie</h2>
<p>Elementy wspomagania, czyli przebieg losowania z punktu widzenia kolejnych kroków wykonywanych przez operatora systemu wspomagania losowania - <em>Panel Helper</em>.</p>
<h3 id="krok-1-przygotowanie-do-losowania">Krok 1. Przygotowanie do losowania</h3>
<p>Podczas przygotowania należy ustalić, które kryteria mogą zostać spełnione w trakcie losowania. Jeśli np. do panelu zgłosiło się mało osób z wykształceniem średnim, to można pominąć parametr wykształcenie.</p>
<p>System prezentuje dwie wizualizacje (dla porównania):</p>
<ul>
<li>Teoretycznego rozkładu (opracowanego na podstawie danych statystycznych).</li>
<li>Rzeczywistego rozkładu (z danych profilowych z ankiet).</li></ul>
<p>Operator decyduje jakie kryteria zostaną użyte przy losowaniu i tworzy profile statystyczne używane później przy losowaniu.</p>
<p>Na tym etapie administrator ustawia panel w tryb testowy, umożliwiając przejrzenie całego systemu. Dlatego dane osobowe powinny być zanonimizowane.   </p>
<h3 id="krok-2-losowanie-grupy-g-wnej-i-zast-pczej">Krok 2. Losowanie grupy głównej i zastępczej</h3>
<h4 id="przygotowanie-przed-wej-ciem-na-ywo">Przygotowanie przed wejściem na żywo</h4>
<ol>
<li>Administrator ustawia panel w tryb losowania i resetuje wybór z testowych losowań.</li>
<li>Administrator wprowadza do systemu prawdziwe dane z rejestracji.</li>
<li>Administrator wprowadza profile do bazy danych.</li>
<li>Operator wchodzi w <em>Wyszukiwanie &rarr; w puli</em>. Operator musi upewnić się, że &quot;Przydział do grup&quot; jest pusty.</li>
<li>Operator wchodzi w <em>Historia &rarr; Historia działań</em> i czyści swoją historię.</li></ol>
<h4 id="etap-na-ywo">Etap na żywo</h4>
<ol>
<li>Operator wchodzi w <em>Wyszukiwanie &rarr; w puli</em>.<ol>
<li>Upewnia się, że &quot;Przydział do grup&quot; jest pusty (wszyscy powinni być <em>w puli</em>).</li>
<li>Upewnia się, że &quot;Historia działań&quot; jest pusta.</li></ol>
</li>
<li>Operator wprowadza profil jako kryteria wyszukiwania. Operator ma swobodę rezygnacji z dowolnych kryteriów (ignorowania ich).</li>
<li>Wyświetlane są wszystkie dopasowane profile.</li>
<li>Operator może zignorować jeden z parametrów statystycznych w wypadku zbyt małej liczby osób do przeprowadzenia losowania.</li>
<li>Komisja losowania wykonuje rzut kostką losując osobę z listy.</li>
<li>Operator przenosi wylosowaną osobę do grupy głównej.</li>
<li>Z tej samej listy losowana jest <em>przynajmniej</em> 1 osoba do grupy zastępczej.</li></ol>
<p>Dane kontaktowe są widoczne dopiero na koniec i tylko dla operatora. Jedynie dana osoba może zweryfikować kiedy została wylosowana (zna swój identyfikator).</p>
<h3 id="krok-3-wyniki">Krok 3. Wyniki</h3>
<ol>
<li>Administrator przełącza system w tryb wyników. Blokowana jest możliwość zmiany grupy i staje się możliwe wyświetlenie listy wylosowanych.</li>
<li>Do publicznej wiadomości podawana jest lista imion i nazwisk posortowana alfabetycznie.</li>
<li>Operator ma możliwość podejrzenia danych kontaktowych tych osób, aby powiadomić ich i upewnić się, że będą na spotkaniach. </li>
<li>Istnieje możliwość wyświetlenia statystyk dla poszczególnych grup (sprawdzenia ostatecznego rozkładu).</li></ol>
